package main

import (
	"fmt"
	"sync"
)

type Fetcher interface {
	// Fetch returns the body of URL and
	// a slice of URLs found on that page.
	Fetch(url string) (body string, urls []string, err error)
}

// Crawl uses fetcher to recursively crawl
// pages starting with url, to a maximum of depth.
func Crawl(url string, depth int, fetcher Fetcher, wg *sync.WaitGroup, prevFetchedUrls *map[string]struct{}, mu *sync.Mutex) {
	// TODO: Fetch URLs in parallel.
	// TODO: Don't fetch the same URL twice.
	// This implementation doesn't do either:
	defer wg.Done()

	if depth <= 0 {
		return
	}

	mu.Lock()
	if _, ok := (*prevFetchedUrls)[url]; ok {
		mu.Unlock()
		return
	}
	(*prevFetchedUrls)[url] = struct{}{}
	mu.Unlock()

	body, urls, err := fetcher.Fetch(url)
	if err != nil {
		fmt.Println(err)
		return
	}
	fmt.Printf("found: %s %q\n", url, body)
	for _, u := range urls {
		wg.Add(1)
		go Crawl(u, depth-1, fetcher, wg, prevFetchedUrls, mu)
	}
	// struct{} is the data type {} is the value
	(*prevFetchedUrls)[url] = struct{}{}
}

func main() {
	var urls = make(map[string]struct{})
	// using mutex to lock the map, using map for constant time lookups. (previously used slice but with the amount of blocking you could argue quicker with a channel for small datasets but for large datasets a map is quicker.)
	var mu sync.Mutex
	var wg sync.WaitGroup
	wg.Add(1)
	Crawl("https://golang.org/", 5, fetcher, &wg, &urls, &mu)
	wg.Wait()
	fmt.Printf("%v", urls)
}

